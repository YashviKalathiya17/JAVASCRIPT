//Operators in JavaScript

// Arithmetic Operators
let a = 10;
let b = 5;

let sum = a + b;
// using the third variable
console.log(sum);
// without using the  third variable
console.log("a=", a, "b=", b);
console.log("a+b=", a + b);
console.log("a-b=", a - b);
console.log("a*b=", a * b);
console.log("a/b=", a / b);
console.log("a%b=", a % b);

// Unary Operators

// Increment and Decrement Operators
// Increment Operator
let x = 23;
console.log("x=", x);
x++;
console.log(x);

// Decrement Operator
// x--;
--x;
console.log(x);

