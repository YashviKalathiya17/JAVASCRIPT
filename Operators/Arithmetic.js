// Arithmetic Operators
let a = 10;
let b = 5;

let sum = a + b;
// using the third variable
console.log(sum);
// without using the  third variable
console.log("a =", a, "b =", b);
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a % b = ", a % b);