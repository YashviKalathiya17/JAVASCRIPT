//Comparison Operators
a = 10;
b = 5;

// Equal to
console.log("a == b : ", a == b);
// Not equal to
console.log("a != b : ", a != b);
// Strict equal to
console.log("a === b : ", a === b);
// Strict not equal to
console.log("a!==b : ", a !== b);
// Greater than
console.log("a > b : ", a > b);
// Less than
console.log("a < b : ", a < b);
// Greater than or equal to
console.log("a >= b : ", a >= b);
// Less than or equal to
console.log("a <= b : ", a <= b);
// Compare with null
console.log("a == null : ", a == null);
// Compare with undefined
console.log("a == undefined : ", a == undefined);
// Compare with NaN
console.log("a == NaN : ", a == NaN);

